package test;

import java.util.List;

public class User {
	private String name;
	
	private String[] hobby;
	
	private Integer[] ha;
	
	private int[] gaga;
	
	private List<Long> ga;
	
	private Integer age;
	
	private int ages;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String[] getHobby() {
		return hobby;
	}

	public void setHobby(String[] hobby) {
		this.hobby = hobby;
	}

	public Integer[] getHa() {
		return ha;
	}

	public void setHa(Integer[] ha) {
		this.ha = ha;
	}

	public List<Long> getGa() {
		return ga;
	}

	public void setGa(List<Long> ga) {
		this.ga = ga;
	}

	public int[] getGaga() {
		return gaga;
	}

	public void setGaga(int[] gaga) {
		this.gaga = gaga;
	}

	public Integer getAge() {
		return age;
	}

	public void setAge(Integer age) {
		this.age = age;
	}

	public int getAges() {
		return ages;
	}

	public void setAges(int ages) {
		this.ages = ages;
	}
	
}
